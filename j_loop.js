// LOOP
// Als we weten hoe vaak we iets willen herhalen kunnen we een for-loop gebruiken.
// In een for-loop wordt de code herhaaldelijk uitgevoerd totdat de voorwaarde niet meer waar is.

// initialisatie: "let i = 0;" maak een variabele die zich gedraagt als een teller en zet deze op 0.
// voorwaarde: "i < 10;" de lus blijft zich herhalen tot de teller niet meer aan de voorwaarde voldoet.
// update: "i++" elke keer als de lus wordt uitgevoerd wordt de teller opgehoogd.

for (let i = 0; i < 10; i++) {
    console.log(i);
}

// In het bovenstaande voorbeeld zal console.log() 10 keer worden aangeroepen. Dit komt omdat een for-lus een teller
// gebruikt als een voorwaarde en deze wordt elke 'loop' opgehoogd en opnieuw gecontroleerd.

// Naast for-loops zijn er ook while- en do-while-loops. Deze varianten worden bijna nooit gebruikt.